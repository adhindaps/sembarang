                            <h2 class="blog-title">ANIMASI</h2>
    <p>Program studi animasi dibentuk dengan aspek kemanfaatan mengembangkan sumber daya bangsa: baik manusia, alam, budaya, seni dan karakter lokal bercirikan ke-Indonesiaan yang mendunia. Hal ini terwujud dalam peran sertanya membangun sumber daya manusia yang kompeten dalam segi keahlian dan berkarakter. Bagi institusi, program studi animasi berperan sebagai sebuah pioner pendidikan tinggi di bidang animasi yang resmi, sekaligus perwujudan pendidikan seni yang multidisipliner.
         Sedangkan bagi masyarakat dan bangsa, program studi animasi memiliki peran sebagai penghasil lulusan setingkat ahli madya yang handal, inovatif dan memiliki keahlian animasi sesuai dengan kompetensi dunia kerja. </p>
        <cite></cite></blockquote>
        
        <h3 class="h4 mt-40"></h3>
        <p>Profil lulusan Program studi Animasi dibagi menjadi dua buah keahlian kompetensi animasi, 
            yaitu di bidang film animasi dan animasi game(animasi interaktif). Selain menghasilkan lulusan handal dan profesional di bidangnya, dari masing-masing bidang dijabarkan pada kebutuhan profesi di dunia kerja bidang animasi, yaitu sebagai seniman papan cerita(storyboard artist), pengatur/perancang kunci animasi(Animator keyframe), pembuat animasi inbetween (Animator inbetweener), seniman pembuat karakter/model dwimatra(2D artist), seniman pembuat karakter/model trimatra(3D artist), perancang tokoh/karakter(character designer), perancang tekstur animasi(texture artist), 
            seniman animasi game(game animation artist), perancang game tingkat madya(game designer), 
            pembuat game tingkat madya(game programmer), penulis skenario(script writer), sutradara animasi(animation director), penanggung jawab gerakan animasi 2D(2D animator), penanggung jawab gerakan animasi 3D(3D animator) dan kebutuhan lainnya. Semua bidang profesi tersebut mendasari setiap mata kuliah dan kurikulum yang diterapkan di program studi ini, sehingga dapat saling menyatu dan komprehensif mendukung bidang keahlian tersebut.</p>
       
