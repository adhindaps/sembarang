        <p>Pekerjaan-pekerjaan dibidang multimedia memang bisa dibilang sesuatu yang baru, yang berkembang dan muncul setelah semakin berkembangnya teknologi informasi seperti adanya internet, komputer, kamera, gadget, dan berbagai perangkat teknologi komunikasi lainnya. Untuk bisa menemukan pelung usaha bidang multimedia sebenarnya cukup mudah, yakni dengan cara merespons perkembangan teknologi informasi yang ada saat ini, yang sebelumnya hanya sebagai penikmat bagaimana bisa menjadi pembuat, dari pembeli bisa menjadi penjual, dari sekedar penonton menjadi sesuatu yang ditonton, dan lain sebagainya. Pada prinsipnya adalah bagaimana merubah peran kita, yang sebelumnya hanya sebagai pelaku konsumtif dirubah menjadi sesuatu yang produktif di bidang mulltmedia. Sesuai dengan namanya Multimedia, seseorang yang menekuni bidang ini justru biasanya memiliki banyak peluang karena dapat bekerja dengan banyak media. Tinggal pilih salah satu media yang dikuasai, kemudian dikembangkan dan dimaksimalkan sehingga menjadi sebuah keahlian yang bernilai tinggi. Adapaun peluang usaha bidang multimedia yang bisa dipilih dan dikembangkan misalnya:

