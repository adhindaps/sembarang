                            <h2 class="blog-title">MULTIMEDIA</h2>
    <p>Definisi Multimedia adalah penggunaan komputer untuk menyajikan dan menggabungkan teks, suara, gambar, animasi, audio dan video dengan alat bantu (tool) dan koneksi (link) sehingga pengguna dapat melakukan navigasi, berinteraksi, berkarya dan berkomunikasi. Multimedia sering digunakan dalam dunia informatika. Selain dari dunia informatika, multimedia juga diadopsi oleh dunia game, dan juga untuk membuat website.

        Multimedia dimanfaatkan juga dalam dunia pendidikan dan bisnis. Di dunia pendidikan, multimedia digunakan sebagai media pengajaran, baik dalam kelas maupun secara sendiri-sendiri atau otodidak. Di dunia bisnis, multimedia digunakan sebagai media profil perusahaan, profil produk, bahkan sebagai media kios informasi dan pelatihan dalam sistem e-learning.</p>
        <cite></cite></blockquote>
        <p>Hingga saat ini, Multimedia masih merupakan salah satu Kompetensi Keahlian di SMK yang paling banyak diminati karena mempelajari hal-hal yang sesuai dengan perkembangan teknologi terkini. Hal ini dikarenakan Kompetensi Keahlian Multimedia mempunyai ruang lingkup pekerjaan dan profesi yang luas, bahkan para siswa lulusan Kompetensi Keahlian 
            Multimedia juga memiliki peluang untuk membuka usaha atau berwirausaha sendiri dalam bidang multimedia.</p>
