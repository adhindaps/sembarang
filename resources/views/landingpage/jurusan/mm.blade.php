                            <p>Definisi Multimedia adalah penggunaan komputer untuk menyajikan dan menggabungkan teks,
                                suara, gambar, animasi, audio dan video dengan alat bantu (tool) dan koneksi (link)
                                sehingga pengguna dapat melakukan navigasi, berinteraksi, berkarya dan berkomunikasi.
                                Multimedia sering digunakan dalam dunia informatika. Selain dari dunia informatika,
                                multimedia juga diadopsi oleh dunia game, dan juga untuk membuat website.

                                Multimedia dimanfaatkan juga dalam dunia pendidikan dan bisnis. Di dunia pendidikan,
                                multimedia digunakan sebagai media pengajaran, baik dalam kelas maupun secara
                                sendiri-sendiri atau otodidak. Di dunia bisnis, multimedia digunakan sebagai media
                                profil perusahaan, profil produk, bahkan sebagai media kios informasi dan pelatihan
                                dalam sistem e-learning.</p>
