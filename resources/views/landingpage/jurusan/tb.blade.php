                            <h2 class="blog-title">TATA BOGA</h2>
    <p>Tata boga adalah suatu disiplin ilmu terkait dengan seni dalam menyiapkan, memasak, dan menghidangkan makanan siap saji. Di Indonesia, disiplin ilmu ini dapat dipelajari di Sekolah Menengah Kejuruan (SMK) atau di tingkat sekolah tinggi terkait Industri pariwisata dan industri perhotelan. Seorang yang memiliki keahlian dalam tata boga dinamakan sebagai koki, juru masak atau chef,
         dimana orang ini diharuskan untuk memiliki pengetahuan dalam ilmu pangan, nutrisi, dan diet serta memiliki kreativitas dalam menyiapkan hidangan makanan yang menarik untuk dilihat. Ilmu tata boga sangat terkait dengan industri pariwisata dan perhotelan dimana kebanyakan para juru masak bekerja di rumah makan, hotel, atau taman hiburan</p>
        <cite></cite>Pekerjaan Menjanjikan untuk Lulusan SMK Jurusan Tata Boga</blockquote>
        <p>Bukan rahasia jika industri kuliner tidak pernah mati. Oleh karena itu, jurusan tata boga tetap menjadi salah satu jurusan yang paling diminati oleh industri saat ini. 
            Lowongan pekerjaan yang tersedia pun beragam. Berikut ini beberapa pekerjaan yang cocok untuk para lulusan SMK jurusan tata boga.</p>
        <h3 class="h4 mt-40">1. Asisten Koki</h3>
        <p>Menjadi asisten koki merupakan suatu pekerjaan yang sangat cocok untuk lulusan SMK tata boga. 
            Dengan kemampuannya di bidang kuliner, lulusan SMK tata boga tentu dapat menjadi asisten koki yang baik.
             Bukan hanya di restaurant saja, kamu juga bisa menjadi asisten koki di hotel berbintang dengan gaji yang besar.</p>
        <h3 class="h4 mt-40">2. Waiters atau Pelayan Restaurant</h3>
        <p>Pekerjaan yang cocok untuk lulusan SMK jurusan tata boga selanjutnya adalah menjadi waiters atau pelayan restaurant. Sama halnya dengan asisten koki, kamu juga bisa menjadi pelayanan restaurant di sebuah hotel. Jika kamu suka dengan dunia perhotelan dan bertemu dengan orang baru, tidak ada salahnya untuk mencoba pekerjaan ini.

            Umumnya pekerjaan menjadi waiters atau pelayan restaurant meliputi memberikan pelayanan pada setiap tamu yang datang, menjaga dan memelihara kebersihan area restaurant, serta menjalankan prosedur yang diterapkan oleh restaurant tersebut. 
            Selain mendapatkan gaji bulanan, kamu juga bisa mendapatkan service charge dan tips jika bekerja menjadi waiters.</p>
        <h3 class="h4 mt-40">3. Juru Masak Rumah Sakit</h3>
            <p>Jika kamu tertarik untuk bekerja di rumah sakit, tidak ada salahnya untuk mencoba pekerjaan yang satu ini. Sama halnya dengan juru masak di restaurant, juru masak di rumah sakit juga bertugas menyiapkan hidangan. Hal yang membuatnya berbeda tentu menu yang dihidangkan. Karena diberikan pada pasien rumah sakit,
                 menu yang diberikan tentu berbeda dengan restaurant.
                Umumnya pekerjaan yang cocok untuk lulusan SMK jurusan 
                tata boga ini juga meliputi perencanaan kerja, agar sesuai dengan menu dan jadwal pembagian makanan ke pasien serta mengkonsultasikan bahan makanan sebelum diolah. Selain itu, kamu juga biasanya bertanggung jawab dalam pengangkutan sampah domestik ke TPS terdekat.</p>
        <h3 class="h4 mt-40">4. Baker</h3>
                <p>Selanjutnya, kamu juga bisa menjadi seorang baker atau bekerja di sebuah toko roti atau bakery. Jika kamu tertarik dengan dunia bakery dibanding memasak menu-menu makanan di restaurant, menjadi seorang baker atau staff di
                     sebuah toko roti bisa menjadi pilihan yang tepat. Gajinya pun tidak kalah dengan beberapa pekerjaan di atas.
                   </p>
        <h3 class="h4 mt-40">5. Staff Catering</h3>
                    <p>PBekerja menjadi staff catering bisa menjadi pilihan lain sebagai lulusan SMK jurusan tata boga. Saat ini ada banyak perusahaan yang membutuhkan staff catering baik untuk memenuhi kebutuhan pasar atau untuk memenuhi kebutuhan di lingkungan perusahaan itu sendiri. 
                        Pendapatan yang diperoleh pun tidak kalah dengan pekerjaan lainnya.</p>
                                 
